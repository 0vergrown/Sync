package dev.overgrown.sync.mixin.extra_limbs;

import dev.overgrown.sync.client.render.feature.ExtraLimbsFeatureRenderer;
import dev.overgrown.sync.client.render.feature.PlayerTextureOverlayFeatureRenderer;
